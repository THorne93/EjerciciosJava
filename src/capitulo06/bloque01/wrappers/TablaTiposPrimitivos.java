package capitulo06.bloque01.wrappers;


//(byte, int, short, long, float, double). 
public class TablaTiposPrimitivos {

	public static void main(String[] args) {

		System.out.println("Byte min/max y tamaño");
		System.out.println(Byte.MIN_VALUE);
		System.out.println(Byte.MAX_VALUE);
		System.out.println(Byte.SIZE);

		
		System.out.println("\nInteger min/max y tamaño");
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Integer.MAX_VALUE);
		System.out.println(Integer.SIZE);

		System.out.println("\nShort min/max y tamaño");
		System.out.println(Short.MIN_VALUE);
		System.out.println(Short.MAX_VALUE);
		System.out.println(Short.SIZE);

		System.out.println("\nLong min/max y tamaño");
		System.out.println(Long.MIN_VALUE);
		System.out.println(Long.MAX_VALUE);
		System.out.println(Long.SIZE);
		
		System.out.println("\nFloat min/max y tamaño");
		System.out.println(Float.MIN_VALUE);
		System.out.println(Float.MAX_VALUE);
		System.out.println(Float.SIZE);

		System.out.println("\nDouble min/max y tamaño");
		System.out.println(Double.MIN_VALUE);
		System.out.println(Double.MAX_VALUE);
		System.out.println(Double.SIZE);



	}
}
